func Normalize(raw string) string {
	key := key(raw)
	if key == "" {
		return Default
	}
	if region, ok := aliases[key]; ok {
		return region
	}
	return Default
}

func YandexLR(region string) string {
	normalized := Normalize(region)
	if lr := yandexLR[normalized]; lr != "" {
		return lr
	}
	return yandexLR[Default]
}

func WildberriesDest(region string) string {
	normalized := Normalize(region)
	if dest := wildberriesDest[normalized]; dest != "" {
		return dest
	}
	return wildberriesDest[Default]
}

func key(raw string) string {
	raw = strings.ToLower(strings.TrimSpace(raw))
	raw = strings.ReplaceAll(raw, "ё", "е")

	parts := strings.FieldsFunc(raw, func(r rune) bool {
		return !(unicode.IsLetter(r) || unicode.IsDigit(r))
	})
	return strings.Join(parts, "")
}
